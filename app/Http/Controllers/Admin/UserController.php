    {

        $user->durum = 1;
        $saved = $user->save();

        if ($saved)
            $notification = array(
                'messege' => 'Düzenleme Başarılı',
                'alert-type' => 'success'
            );
        else
            $notification = array(
                'messege' => 'Dikkat ! Bir Hata Oluştu',
                'alert-type' => 'error'
            );

        return back()->with($notification);
    }

